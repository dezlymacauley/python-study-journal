my_string = "5"
my_int = 7
my_float = 5.5
my_bool = True

# NOTE: In Python it is possible to add and int and a float
int_plus_float = my_int + my_float
print(f"{int_plus_float}")

# NOTE: This is how you convert data types
print(int(my_string))
print(str(my_int))

# NOTE: You can also print out the type of a variable
print(type(my_string))
# <class 'str'>

print(type(my_int))
# <class 'int'>

print(type(my_float))
# <class 'float'>

print(type(my_bool))
# <class 'bool'>
