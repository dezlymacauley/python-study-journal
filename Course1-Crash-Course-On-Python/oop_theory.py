# NOTE: What is oop?

# Object oreintated programming

# OOP is a flexible, powerful paradigm where classes represent,
# and define concepts, while objects are instances of classes

# Attributes are the characteristics associated to a type
# Methods are the functions associated to a type

# Real-world concepts are represented by classes
# Instances of classes are usually called objects

# Objects have attributes which are used to store information about them.
# We can make objects do work by calling their methods

# E.g. Let's say we have an Apple 
# Its attributes could be things like its flavor and color

# NOTE: What is a method?

# Methods are functions that operate on the attributes 
# of a specific instance of a class

# It's methods could be things like "cut" that cuts the apple
# Or "reduce" the size of the apple

