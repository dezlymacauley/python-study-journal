# a + b = Adds a and b
# a - b = Subtracts b from a

# a * b = Multiplies a and b
# a / b = Divides a by b

# a ** b = Elevates a to the power of b. For non integer values of b,
# this becomes a root (i.e. a**(1/2) is the square root of a)

# a // b = Returns the integer part of the integer division by b
# This is also known as floor division


# a % b = Returns the remainder part of the integer division by b
